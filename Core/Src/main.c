typedef enum
{
  SPI_MODE_MASTER_RX,
  SPI_MODE_MASTER_TX,
  SPI_MODE_SLAVE_RX,
  SPI_MODE_SLAVE_TX
} SPI_Mode;

void SPI_Switch_Mode(SPI_HandleTypeDef *hspi, SPI_Mode mode)
{
  // Note: To modify the HDDIR bit, the SPE (SPI Peripheral Enable) bit must be cleared.
  __HAL_SPI_DISABLE(hspi);

  // HDDIR = 0(RX)
  // HDDIR = 1(TX)
  switch (mode)
  {
    case SPI_MODE_MASTER_RX:
    case SPI_MODE_SLAVE_RX:
      CLEAR_BIT(hspi->Instance->CR1, SPI_CR1_HDDIR);
      break;

    case SPI_MODE_MASTER_TX:
    case SPI_MODE_SLAVE_TX:
      SET_BIT(hspi->Instance->CR1, SPI_CR1_HDDIR);
      break;

    default:
      return;
  }

  // Note: The HAL API functions for SPI Transmit and Receive
  // internally set the SPE (SPI Peripheral Enable) bit.
  // Avoid redundant use of __HAL_SPI_ENABLE(hspi) to prevent conflicts.

  //__HAL_SPI_ENABLE(hspi);
}

void SPI_LoopBackTest(void)
{
  // SPI communication from Master to Slave:
  // 1. Set SPI1 to Master TX mode and SPI2 to Slave RX mode.
  // 2. Initialize non-blocking SPI transmission (Master) and reception (Slave).
  // 3. Wait until the Slave receives data (dataReceivedFlag_slave is set).
  // 4. Compare the received data in slaveRxBuffer with the transmitted data in masterTxBuffer.
  // 5. If the data matches, clear the slaveRxBuffer and exit the loop.
  // 6. If the data does not match, call the error handler.

  SPI_Switch_Mode(&hspi1, SPI_MODE_MASTER_TX); // Switch SPI1 to Master TX mode
  SPI_Switch_Mode(&hspi2, SPI_MODE_SLAVE_RX);  // Switch SPI2 to Slave RX mode

  if (HAL_SPI_Receive_IT(&hspi2, slaveRxBuffer, sizeof(slaveRxBuffer)) != HAL_OK)
  {
    Error_Handler();
  }

  if (HAL_SPI_Transmit_IT(&hspi1, masterTxBuffer, sizeof(masterTxBuffer)) != HAL_OK)
  {
    Error_Handler();
  }

  while (1)
  {
    if (dataReceivedFlag_slave)
    {
      dataReceivedFlag_slave = 0;

      // Compare the slave RX buffer with the master TX buffer
      if (memcmp(masterTxBuffer, slaveRxBuffer, sizeof(masterTxBuffer)) == 0)
      {
        memset(slaveRxBuffer, 0, sizeof(slaveRxBuffer));
        break;
      }
      else
      {
        Error_Handler();
      }
    }
  }

  // SPI communication from Slave to Master:
  // 1. Configure SPI1 (Master) to RX mode and SPI2 (Slave) to TX mode.
  // 2. Start non-blocking SPI transmission from Slave (slaveTxBuffer).
  // 3. Start non-blocking SPI reception on Master (masterRxBuffer).
  // 4. Enter a loop to wait for the Master to complete data reception.

  SPI_Switch_Mode(&hspi1, SPI_MODE_MASTER_RX); // Switch SPI1 to Master RX mode
  SPI_Switch_Mode(&hspi2, SPI_MODE_SLAVE_TX);  // Switch SPI2 to Slave TX mode

  if (HAL_SPI_Transmit_IT(&hspi2, slaveTxBuffer, sizeof(slaveTxBuffer)) != HAL_OK)
  {
    Error_Handler();
  }

  if (HAL_SPI_Receive_IT(&hspi1, masterRxBuffer, sizeof(masterRxBuffer)) != HAL_OK)
  {
    Error_Handler();
  }

  while (1)
  {
    if (dataReceivedFlag_master)
    {
      dataReceivedFlag_master = 0;

      // Compare the master RX buffer with the slave TX buffer
      if (memcmp(slaveTxBuffer, masterRxBuffer, sizeof(slaveTxBuffer)) == 0)
      {
        memset(masterRxBuffer, 0, sizeof(masterRxBuffer));
        break;
      }
      else
      {
        Error_Handler();
      }
    }
  }
}

